<p>&emsp;The Chinese zodiac, or sheng xiao, is a repeating cycle of 12 years where each year is represented by an animal and its reputed attributes. 
    The order of the animals does not change and always cycles in this order:
     Rat, Ox, Tiger, Rabbit, Dragon, Snake, Horse, Ram, Monkey, Rooster, Dog, Pig. 
     The selection and order of these animals originated in the Han Dynasty (202BC-220AD) and was based around what animals held influence in the peoples' lives.
      The division into 12 years and 12 animals is related to the old-time division used back then based around the number 12;
       12 months in a year, 12 time periods in a day, and 12 full moons within a year. </p>

<p>&emsp;It is believed that the year you were born will affect your personality in some way, mainly based off of the traits of the animal assigned to that year.
     Here is an example; people born in the year of the Ox are diligent, dependable, strong, and determined.
      While many in the western parts of the world see a lot of this as superstitious, Chinese people take their year of birth seriously.
       In the old folk lore it is said that people who reject these ideas may offend Taisui, the God of Age, and suffer many misfortunes and one way to,
        hopefully, avoid this was to wear red colored clothing and accessories, for red is believed to bring good luck.</p>

<p>&emsp;You might be wondering "How does this compare to western astrology?"
     Well, the main similarity is that both systems are based on date and time of birth,
      with 12 symbols or signs used to communicate meaning.
       While they do both have 12 symbols,
        most of them are different, though two are superficially similar:
             Ox/Bull and Goat/Ram. These signs are also different in their origins;
              in Chinese astrology, the 12 signs come from a myth that when the gods were developing the calender they summoned one of each animal for a race,
               and the 12 who finished first became the zodiac signs, while in Western astrology the signs are based on constellations' positions relative to the earth and named according to Greek mythology.
                In some ways, the Chinese zodiac is more complex since it follows an idea of four pillars,
                 your birth year is one of them but also your birth month, day, and hour are important,
                  while in Western astrology it mainly focuses on the celestial alignment of when you were born.
                   One thing I haven't mentioned is that each system also involves the elements.
                    In Chinese astrology there are five elements: fire, earth, metal, water, and wood.
                     These elements are linked to the motivating forces in your life,
                      and since each 12 year cycle is assigned to one of these elements,
                       the full zodiac-element cycle lasts 60 years! In the western system there are only four elements:
                        fire, earth, air, and water and each sign is associated with one of these elements.</p>
