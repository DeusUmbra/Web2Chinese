<h2>Web Survey</h2>
<p>&emsp;This script gives the user a survey, question by question, on how they felt about this site, using an array to hold and display the questions
and using session storage to hold the responses. Once the user is finished, their responses are displayed and emailed off to the creator of this site.</p>
